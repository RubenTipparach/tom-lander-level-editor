// Picotron 32-color palette. Index 0..31 maps to terrain height.
// Mirrors PicotronPalette.cs in the C# editor.
export const PALETTE = [
  [0x00,0x00,0x00], [0x1D,0x2B,0x53], [0x7E,0x25,0x53], [0x00,0x87,0x51],
  [0xAB,0x52,0x36], [0x5F,0x57,0x4F], [0xC2,0xC3,0xC7], [0xFF,0xF1,0xE8],
  [0xFF,0x00,0x4D], [0xFF,0xA3,0x00], [0xFF,0xEC,0x27], [0x00,0xE4,0x36],
  [0x29,0xAD,0xFF], [0x83,0x76,0x9C], [0xFF,0x77,0xA8], [0xFF,0xCC,0xAA],
  [0x1C,0x5E,0xAC], [0x00,0xA5,0xA1], [0x75,0x4E,0x97], [0x12,0x53,0x59],
  [0x74,0x2F,0x29], [0x49,0x2D,0x38], [0xA2,0x88,0x79], [0xFF,0xAC,0xC5],
  [0xC3,0x00,0x4C], [0xEB,0x6B,0x00], [0x90,0xEC,0x42], [0x00,0xB2,0x51],
  [0x64,0xDF,0xF6], [0xBD,0x9A,0xDF], [0xE4,0x0D,0xAB], [0xFF,0x85,0x6D],
];

const EXACT = new Map();
for (let i = 0; i < 32; i++) {
  const [r,g,b] = PALETTE[i];
  EXACT.set((r << 16) | (g << 8) | b, i);
}

export function findExact(r, g, b) {
  const k = (r << 16) | (g << 8) | b;
  return EXACT.has(k) ? EXACT.get(k) : -1;
}

export function findNearest(r, g, b) {
  let best = 0, bd = Infinity;
  for (let i = 0; i < 32; i++) {
    const [pr,pg,pb] = PALETTE[i];
    const dr = pr - r, dg = pg - g, db = pb - b;
    const d = dr*dr + dg*dg + db*db;
    if (d < bd) { bd = d; best = i; }
  }
  return best;
}

export function colorToCss(idx) {
  const [r,g,b] = PALETTE[idx & 31];
  return `rgb(${r},${g},${b})`;
}

// Pixels darker than mid grey → label with light text on swatch
export function isDarkSwatch(idx) {
  const [r,g,b] = PALETTE[idx & 31];
  return (r * 0.299 + g * 0.587 + b * 0.114) < 128;
}
